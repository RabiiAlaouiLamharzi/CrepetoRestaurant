    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try (Connection conn = DriverManager.getConnection(url, uid, pw)) {

            // the insert statement
            String qry = "INSERT INTO Employee "
            + " (employee_id, employee_name, employee_address, employee_phone,"
            + " employee_email, employee_salary)"
            + " VALUES (?, ?, ?, ?, ?, ?);";

            // create the insert preparedstatement
            PreparedStatement prepStmt = conn.prepareStatement(qry);
            prepStmt.setInt (1, Integer.parseInt(jTextField1.getText()));
            prepStmt.setString (2, jTextField2.getText());
            prepStmt.setString (3, jTextField3.getText());
            prepStmt.setInt (4, Integer.parseInt(jTextField4.getText()));
            prepStmt.setString (5, jTextField6.getText());
            prepStmt.setFloat  (6, Float.parseFloat(jTextField5.getText()));

            // execute the preparedstatement
            prepStmt.execute();
            jLabel6.setText("Employee added! ");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");

        } catch (SQLException ex) {
            System.err.println("SQLException: " + ex);
            jLabel6.setText("SQLException: " + ex);
        }
        catch (Exception e) {
            System.err.println("Exception: " + e);
            jLabel6.setText("Exception: " + e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

