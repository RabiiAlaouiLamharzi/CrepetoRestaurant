    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try (Connection conn = DriverManager.getConnection(url, uid, pw)) {

            // the insert statement
            String qry = "INSERT INTO Orders "
            + " (order_id, order_name, order_date, order_quantity, order_price, product_id)"
            + " VALUES (?, ?, ?, ?, ?, ?);";

            // create the insert preparedstatement
            PreparedStatement prepStmt = conn.prepareStatement(qry);
            prepStmt.setInt (1, Integer.parseInt(jTextField1.getText()));
            prepStmt.setString (2, jTextField2.getText());
            SimpleDateFormat formatdate = new SimpleDateFormat("yyyy-MM-dd"); 
            java.util.Date utilDate = formatdate.parse(jTextField3.getText()); 
            java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime()); 
            prepStmt.setDate (3, sqlDate); 
            prepStmt.setFloat  (4, Float.parseFloat(jTextField4.getText()));
            prepStmt.setFloat  (5, Float.parseFloat(jTextField6.getText()));
            
            String product_id = jTextField5.getText(); 
            if (product_id.equals("")) 
            prepStmt.setNull (6, Types.INTEGER); 
            else 
            prepStmt.setInt (6, Integer.parseInt(jTextField5.getText()));

            // execute the preparedstatement
            prepStmt.execute();
            jLabel6.setText("Order added! ");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");

        } catch (SQLException ex) {
            System.err.println("SQLException: " + ex);
            jLabel6.setText("SQLException: " + ex);
        }
        catch (Exception e) {
            System.err.println("Exception: " + e);
            jLabel6.setText("Exception: " + e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

